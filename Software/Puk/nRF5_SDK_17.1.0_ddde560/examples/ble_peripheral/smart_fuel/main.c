/**@brief Function for handling the idle state (main loop).
 *
 * @details If there is no pending log operation, then sleep until next the next event occurs.
 */
static void idle_state_handle(void)
{
    if (NRF_LOG_PROCESS() == false)
    {
        nrf_pwr_mgmt_run();
    }
    delete_all_process();
    
}


void hx711_callback(hx711_evt_t evt, int value)
{
    uint16_t length = sizeof(int);

    
    
    if(evt == DATA_READY)
    {
        /* Transmit received sensor data over BLE - ignoring return value. Packets will be dropped
         if:
         -  Not connected
         -  Client has not enabled Notification on the TX characteristic
         -  TX buffer is full*/
        if(!hx711_offset_set) {
          hx711_offset_set = true;
          hx711_offset = value;

        } else{
          int value_with_offset = (value - hx711_offset);
          uint32_t weigth = 0;
          if (value_with_offset > 0) {
            weigth = value_with_offset / 216;
          } 
          if(!wait_for_delete) {
            uint8_t rounded_value = round(weigth / 10.0f);
            write_boot_count(rounded_value);
          } else {
            NRF_LOG_INFO("Wait for Delete end");
          }
          //ble_lbs_on_button_change(m_conn_handle, &m_lbs, weigth);
          //get_data_information(m_conn_handle, &m_lbs);
          NRF_LOG_INFO("Weight value %d", weigth);
          nrf_gpio_pin_clear(30);
        }
    }
    else
    {
        /*Invalid ADC readout. A non-zero value would indicate that the readout was interrupted 
         by a higher priority interrupt during readout (i.e., Softdevice radio event).
         */
        NRF_LOG_INFO("ADC readout error. %d 0x%x", value, value);
        write_boot_count(0); // 0 value is noise
    }
}


